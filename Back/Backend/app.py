# TASK 4 — /api/processes  (JSON, top-N, searchable)
# ==========================================================================

@app.route("/api/processes", methods=["GET"])
def api_processes():
    """
    Return JSON list of running processes sorted by cpu_percent desc.
    Query params:
      ?search=<name>   — filter by process name (case-insensitive)
      ?limit=<int>     — max results (default 50, max 200)
    """
    try:
        search: str = request.args.get("search", "").lower()
        limit: int = min(int(request.args.get("limit", 50)), 200)

        processes: List[Dict[str, Any]] = []
        for proc in psutil.process_iter(["pid", "name", "cpu_percent", "memory_percent", "status", "username"]):
            try:
                info = proc.info
                # Apply search filter
                if search and search not in (info.get("name") or "").lower():
                    continue
                processes.append({
                    "pid": info.get("pid"),
                    "name": info.get("name", "Unknown"),
                    "cpu_percent": info.get("cpu_percent", 0.0) or 0.0,
                    "memory_percent": round(info.get("memory_percent", 0.0) or 0.0, 2),
                    "status": info.get("status", "unknown"),
                    "username": info.get("username", "N/A"),
                })
            except (psutil.NoSuchProcess, psutil.AccessDenied, psutil.ZombieProcess):
                continue

        # Sort by CPU desc, slice to limit
        processes.sort(key=lambda p: p["cpu_percent"], reverse=True)
        return jsonify(processes[:limit])
    except Exception as e:
        return jsonify({"error": str(e)}), 500


