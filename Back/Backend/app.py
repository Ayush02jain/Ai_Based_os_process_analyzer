# TASK 3 — In-memory TTL cache
# ==========================================================================

class TTLCache:
    """Simple dict-based cache with per-key TTL (in seconds)."""

    def __init__(self) -> None:
        self._store: Dict[str, Any] = {}
        self._timestamps: Dict[str, float] = {}

    def get(self, key: str, ttl: float) -> Optional[Any]:
        """Return cached value if within TTL, else None."""
        ts = self._timestamps.get(key)
        if ts is not None and (time.time() - ts) < ttl:
            return self._store[key]
        return None

    def set(self, key: str, value: Any) -> None:
        """Store a value and record current timestamp."""
        self._store[key] = value
        self._timestamps[key] = time.time()


cache = TTLCache()

# TTL constants (seconds)
PSUTIL_TTL: float = 1.5   # Cache raw psutil metrics for 1.5 s
ML_TTL: float = 2.0       # Cache ML prediction for 2 s


# ==========================================================================
# System metrics helpers
# ==========================================================================

def get_system_metrics_cached() -> Dict[str, Any]:
    """
    Read CPU / memory / disk / process_count via psutil.
    Returns cached result if within PSUTIL_TTL.
    """
    cached = cache.get("psutil_metrics", PSUTIL_TTL)
    if cached is not None:
        return cached

    cpu = psutil.cpu_percent(interval=0.5)
    memory = psutil.virtual_memory().percent

    # shutil.disk_usage works on Windows (psutil.disk_usage is broken in 5.9.0)
    disk_path = "C:" if os.name == "nt" else "/"
    d_total, d_used, _ = shutil.disk_usage(disk_path)
    disk = round(d_used / d_total * 100, 1)

    process_count = len(list(psutil.process_iter()))

    metrics = {
        "cpu_usage": cpu,
        "memory_usage": memory,
        "disk_usage": disk,
        "process_count": process_count,
    }
    cache.set("psutil_metrics", metrics)
    return metrics


def get_prediction_cached(metrics: Dict[str, Any]) -> Dict[str, Any]:
    """
    Run ML inference on the 4-feature vector.
    Returns cached result if within ML_TTL.
    Includes 'status' (Normal|Anomaly) and 'confidence' (0–1).
    """
    cached = cache.get("ml_prediction", ML_TTL)
    if cached is not None:
        return cached

    import numpy as np
    features = np.array([[
        metrics["cpu_usage"],
        metrics["memory_usage"],
        metrics["disk_usage"],
        metrics["process_count"],
    ]])
    scaled = scaler.transform(features)
    prediction = model.predict(scaled)[0]
    probabilities = model.predict_proba(scaled)[0]
    confidence = float(max(probabilities))

    result = {
        "status": "Normal" if prediction == 1 else "Anomaly",
        "confidence": round(confidence, 4),
    }
    cache.set("ml_prediction", result)
    return result


def build_suggestions(status: str, cpu: float, memory: float, disk: float) -> List[str]:
    """Generate actionable suggestions based on anomaly status and metric values."""
    if status != "Anomaly":
        return ["System is operating normally."]
    suggestions: List[str] = []
    if cpu > 85:
        suggestions.append("High CPU usage detected. Close unnecessary applications.")
    if memory > 85:
        suggestions.append("High memory usage detected. Free up memory by closing unused apps.")
    if disk > 90:
        suggestions.append("Disk usage critically high. Free up disk space.")
    suggestions.append("Check for zombie or orphan processes and terminate them.")
    suggestions.append("Monitor CPU temperature to prevent overheating.")
    return suggestions


def build_full_payload() -> Dict[str, Any]:
    """Assemble the complete metrics + ML + suggestions payload."""
    metrics = get_system_metrics_cached()
    prediction = get_prediction_cached(metrics)
    suggestions = build_suggestions(
        prediction["status"],
        metrics["cpu_usage"],
        metrics["memory_usage"],
        metrics["disk_usage"],
    )
    return {
        **metrics,
        **prediction,
        "suggestions": suggestions,
    }


# ==========================================================================
# TASK 2 — WebSocket: emit metrics_update every 2 seconds
# ==========================================================================

def background_emitter() -> None:
    """
    Runs in a daemon thread. Every 2 seconds it reads system metrics,
    runs ML inference (with caching), and emits 'metrics_update' to
    all connected WebSocket clients.
    """
    while True:
        try:
            payload = build_full_payload()
            socketio.emit("metrics_update", payload)
        except Exception as e:
            print(f"[emitter] Error: {e}")
        socketio.sleep(2)


@socketio.on("connect")
def handle_connect() -> None:
    """Send an immediate update when a client connects."""
    try:
        payload = build_full_payload()
        emit("metrics_update", payload)
    except Exception as e:
        print(f"[connect] Error: {e}")


